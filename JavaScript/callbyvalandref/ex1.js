//How JavaScript Stores Primitive & Non-Primitive Data Types


//Primitive Data Types (Numbers, Strings, Boolean, etc.)

//Stored directly in memory as values.
//When you assign or pass them to a function, a new copy is created.
//Changing the copy does NOT affect the original.


//Non-Primitive Data Types (Objects, Arrays, Functions)

//Stored in heap memory, but the variable only holds a reference (memory address) to the data.
//When you assign or pass an object, only the reference (not the actual object) is copied.
//Modifying the object via the reference affects the original object.
//Reassigning the reference does NOT change the original object.
